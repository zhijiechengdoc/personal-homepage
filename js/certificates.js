let certificateData = [];
let currentCertificateEditIndex = -1;
let certificateDeleteConfirmIndex = -1;

function getCertificateText(text, lang) {
    if (typeof text === 'object' && text !== null) {
        return text[lang] || text.zh || '';
    }
    return text || '';
}

function loadCertificates() {
    const saved = localStorage.getItem('certificates');
    if (saved) {
        try {
            const parsed = JSON.parse(saved);
            if (parsed.length > 0 && typeof parsed[0].title === 'object') {
                certificateData = parsed;
            } else {
                certificateData = JSON.parse(JSON.stringify(defaultCertificateData));
                saveCertificates();
            }
        } catch {
            certificateData = JSON.parse(JSON.stringify(defaultCertificateData));
            saveCertificates();
        }
    } else {
        certificateData = JSON.parse(JSON.stringify(defaultCertificateData));
        saveCertificates();
    }
    renderCertificates();
}

function saveCertificates() {
    localStorage.setItem('certificates', JSON.stringify(certificateData));
}

function renderCertificates() {
    const container = document.getElementById('certificatesContainer');
    if (!container) return;
    const currentLang = localStorage.getItem('language') || 'zh';
    
    container.innerHTML = certificateData.map((cert, index) => `
        <div class="certificate-card">
            <div class="certificate-actions">
                <button class="edit-certificate-btn" onclick="openEditCertificateModal(${index})">✏️</button>
                <button class="delete-certificate-btn" onclick="openDeleteCertificateConfirm(${index})">🗑️</button>
            </div>
            <span class="certificate-icon">${cert.icon}</span>
            <div class="certificate-title">${getCertificateText(cert.title, currentLang)}</div>
            <div class="certificate-competition">${getCertificateText(cert.competition, currentLang)}</div>
            <div class="certificate-info">
                <div class="certificate-info-item">
                    <span class="certificate-info-icon">📍</span>
                    <span>${getCertificateText(cert.level, currentLang)}</span>
                </div>
                <div class="certificate-info-item">
                    <span class="certificate-info-icon">🎯</span>
                    <span>${currentLang === 'zh' ? '项目：' : 'Project: '}${getCertificateText(cert.project, currentLang)}</span>
                </div>
                ${cert.status ? `
                <div class="certificate-info-item">
                    <span class="certificate-info-icon">📋</span>
                    <span>${currentLang === 'zh' ? '状态：' : 'Status: '}${getCertificateText(cert.status, currentLang)}</span>
                </div>
                ` : ''}
            </div>
            <div class="certificate-date">
                <span>📅</span>
                <span>${getCertificateText(cert.date, currentLang)}</span>
            </div>
            <span class="certificate-badge">${getCertificateText(cert.badge, currentLang)}</span>
        </div>
    `).join('');
}

function updateCertificatesLanguage() {
    renderCertificates();
}

function openAddCertificateModal() {
    currentCertificateEditIndex = -1;
    document.getElementById('certificateModalTitle').textContent = '添加证书';
    document.getElementById('certificateIcon').value = '🏆';
    document.getElementById('certificateTitle').value = '';
    document.getElementById('certificateCompetition').value = '';
    document.getElementById('certificateLevel').value = '国家级';
    document.getElementById('certificateProject').value = '';
    document.getElementById('certificateDate').value = '';
    document.getElementById('certificateStatus').value = '';
    document.getElementById('certificateBadge').value = '国家级';
    document.getElementById('certificateModalOverlay').classList.add('active');
}

function openEditCertificateModal(index) {
    currentCertificateEditIndex = index;
    const cert = certificateData[index];
    const currentLang = localStorage.getItem('language') || 'zh';
    document.getElementById('certificateModalTitle').textContent = '编辑证书';
    document.getElementById('certificateIcon').value = cert.icon;
    document.getElementById('certificateTitle').value = getCertificateText(cert.title, currentLang);
    document.getElementById('certificateCompetition').value = getCertificateText(cert.competition, currentLang);
    document.getElementById('certificateLevel').value = getCertificateText(cert.level, currentLang);
    document.getElementById('certificateProject').value = getCertificateText(cert.project, currentLang);
    document.getElementById('certificateDate').value = getCertificateText(cert.date, currentLang);
    document.getElementById('certificateStatus').value = cert.status ? getCertificateText(cert.status, currentLang) : '';
    document.getElementById('certificateBadge').value = getCertificateText(cert.badge, currentLang);
    document.getElementById('certificateModalOverlay').classList.add('active');
}

function closeCertificateModal() {
    document.getElementById('certificateModalOverlay').classList.remove('active');
}

function saveCertificate() {
    const icon = document.getElementById('certificateIcon').value.trim();
    const title = document.getElementById('certificateTitle').value.trim();
    const competition = document.getElementById('certificateCompetition').value.trim();
    const level = document.getElementById('certificateLevel').value;
    const project = document.getElementById('certificateProject').value.trim();
    const date = document.getElementById('certificateDate').value.trim();
    const status = document.getElementById('certificateStatus').value.trim();
    const badge = document.getElementById('certificateBadge').value;
    const currentLang = localStorage.getItem('language') || 'zh';
    
    if (!title || !competition) {
        alert('请填写必填项！');
        return;
    }
    
    const cert = {
        icon: icon || '🏆',
        title: { [currentLang]: title },
        competition: { [currentLang]: competition },
        level: { [currentLang]: level },
        project: { [currentLang]: project },
        date: { [currentLang]: date },
        badge: { [currentLang]: badge }
    };
    
    if (status) {
        cert.status = { [currentLang]: status };
    }
    
    if (currentCertificateEditIndex === -1) {
        certificateData.unshift(cert);
    } else {
        certificateData[currentCertificateEditIndex] = cert;
    }
    
    saveCertificates();
    renderCertificates();
    closeCertificateModal();
}

function openDeleteCertificateConfirm(index) {
    certificateDeleteConfirmIndex = index;
    document.getElementById('certificateDeleteConfirmOverlay').classList.add('active');
}

function closeCertificateDeleteConfirm() {
    document.getElementById('certificateDeleteConfirmOverlay').classList.remove('active');
    certificateDeleteConfirmIndex = -1;
}

function confirmDeleteCertificate() {
    if (certificateDeleteConfirmIndex !== -1) {
        certificateData.splice(certificateDeleteConfirmIndex, 1);
        saveCertificates();
        renderCertificates();
    }
    closeCertificateDeleteConfirm();
}

function exportCertificates() {
    const data = JSON.stringify(certificateData, null, 2);
    const blob = new Blob([data], { type: 'application/json' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'certificates.json';
    a.click();
    URL.revokeObjectURL(url);
}

function importCertificates(event) {
    const file = event.target.files[0];
    if (!file) return;
    
    const reader = new FileReader();
    reader.onload = function(e) {
        try {
            const data = JSON.parse(e.target.result);
            if (Array.isArray(data)) {
                certificateData = data;
                saveCertificates();
                renderCertificates();
                alert('证书导入成功！');
            } else {
                alert('无效的证书数据格式！');
            }
        } catch (err) {
            alert('证书导入失败：' + err.message);
        }
    };
    reader.readAsText(file);
    event.target.value = '';
}

function resetCertificates() {
    if (confirm('确定要重置所有证书数据吗？此操作将恢复为默认数据！')) {
        certificateData = JSON.parse(JSON.stringify(defaultCertificateData));
        saveCertificates();
        renderCertificates();
        alert('证书数据已重置！');
    }
}

document.addEventListener('DOMContentLoaded', loadCertificates);
